import { clsx } from "clsx"
import { twMerge } from "tailwind-merge"

export function cn(...inputs) {
  return twMerge(clsx(inputs))
}

export function formatNumber(num) {
  if (num >= 1000000) {
    return `${(num / 1000000).toFixed(1)}M`
  } else if (num >= 1000) {
    return `${(num / 1000).toFixed(1)}K`
  }
  return num.toString()
}

export function formatDate(date) {
  return new Intl.DateTimeFormat('en-US', {
    year: 'numeric',
    month: 'short',
    day: 'numeric',
  }).format(new Date(date))
}

export function calculateTimeSince(timestamp) {
  const now = new Date()
  const postTime = new Date(timestamp)
  const diffMs = now.getTime() - postTime.getTime()
  const diffDays = Math.floor(diffMs / (1000 * 60 * 60 * 24))
  const diffHours = Math.floor(diffMs / (1000 * 60 * 60))
  const diffMinutes = Math.floor(diffMs / (1000 * 60))

  if (diffMinutes < 60) return `${diffMinutes}m ago`
  if (diffHours < 24) return `${diffHours}h ago`
  if (diffDays === 1) return '1 day ago'
  if (diffDays < 7) return `${diffDays} days ago`
  if (diffDays < 30) return `${Math.floor(diffDays / 7)} weeks ago`
  return `${Math.floor(diffDays / 30)} months ago`
}

export function getProxiedImageUrl(instagramUrl) {
  if (!instagramUrl) return ''
  
  try {
    // Using images.weserv.nl free proxy service
    const encodedUrl = encodeURIComponent(instagramUrl)
    return `https://images.weserv.nl/?url=${encodedUrl}&w=400&h=400&fit=cover&a=attention`
  } catch (error) {
    console.error('Error proxying image:', error)
    return instagramUrl // fallback to original
  }
}